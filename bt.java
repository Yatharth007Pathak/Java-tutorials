/*
if Statement
The if statement checks a condition. If the condition evaluates to true, the block of code inside the if is executed.

Syntax:
if (condition) {
    // Code to be executed if the condition is true
}
*/

public class bt {

    public static void main(String[] args) {
        int x = 10;

        // Check if x is greater than 5
        if (x > 5) {
            System.out.println("x is greater than 5");
        }
    }
}
